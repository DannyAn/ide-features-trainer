
public class DuplicateLineDemo {

    public static void main(String[] args) {

        int a = 7;
        double x = 0.34;

        //Let's look an absolute value for a and x.
        System.out.println("|" + a + "| is " + Math.abs(a));
        System.out.println("|" + x + "| is " + Math.abs(x));
    }
}
